type MaterialSymbolData = {
  left?: number;
  top?: number;
  width?: number;
  height?: number;
  body: string;
};

export type AppIconProps = React.SVGProps<SVGSVGElement> & {
  size?: number | string;
  absoluteStrokeWidth?: boolean;
};
export type AppIcon = React.ForwardRefExoticComponent<
  AppIconProps & React.RefAttributes<SVGSVGElement>
>;
export type LucideIcon = AppIcon;

function createMaterialSymbol(icon: MaterialSymbolData, displayName: string): AppIcon {
  const MaterialSymbol = React.forwardRef<SVGSVGElement, AppIconProps>(
    (
      {
        className,
        "aria-label": ariaLabel,
        role,
        size,
        absoluteStrokeWidth: _absoluteStrokeWidth,
        strokeWidth,
        ...props
      },
      ref
    ) => (
      <svg
        ref={ref}
        width={size ?? "1em"}
        height={size ?? "1em"}
        viewBox={`${icon.left ?? 0} ${icon.top ?? 0} ${icon.width ?? 24} ${icon.height ?? 24}`}
        fill="currentColor"
        focusable="false"
        aria-hidden={ariaLabel ? undefined : true}
        aria-label={ariaLabel}
        role={role ?? (ariaLabel ? "img" : undefined)}
        strokeWidth={strokeWidth}
        className={cn("inline-block shrink-0", className)}
        {...props}
        dangerouslySetInnerHTML={{ __html: icon.body }}
      />
    )
  );

  MaterialSymbol.displayName = displayName;
  return MaterialSymbol;
}

